// User function template for C++
class Solution {
  public:
    int countgroup(vector<int>& arr) {
        // Complete the function
        int xor_value=0;
        for(int i=0;i<arr.size();i++){
            xor_value^=arr[i];
        }
        if(xor_value!=0)return 0;
        return arr.size();
    }
};

