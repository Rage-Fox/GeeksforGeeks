class Solution {
  public:
    int countPairs(vector<int> &arr, int target) {
        // Complete the function
        unordered_map<int,int> mpp;
        int c=0;
        for(int i=0;i<arr.size();i++){
            int cp=target-arr[i];
            if(mpp.find(cp)!=mpp.end()){
                c+=mpp[cp];
            }
            mpp[arr[i]]++;
        }
        return c;
    }
};

